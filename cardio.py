from flask import Flask, request, render_template, jsonify
import joblib
import numpy as np

# Initialize the Flask app
app = Flask(__name__)

# Load the trained model
model = joblib.load('C:/Users/SANIDHYA/OneDrive/Desktop/cardio vascular disease/cardio.model.pkl')

# Route for the homepage (index.html)
@app.route('/')
def index():
    return render_template('index.html')  # Render the HTML file

# Route for prediction
@app.route('/predict', methods=['POST'])
def predict():
    try:
        # Get the input data from the form
        features = [float(request.form['age']),  # Replace with actual feature names
                    float(request.form['gender']),
                    float(request.form['weight']),
                    float(request.form['height']),
                    float(request.form['ap_hi']),
                    float(request.form['ap_lo']),
                    float(request.form['gluc']),
                    float(request.form['cholesterol']),
                    float(request.form['smoke']),
                    float(request.form['alco']),
                    float(request.form['active']),
                     float(request.form['cardio'])]


        # Reshape the input features to match the model input
        features = np.array(features).reshape(1, -1)

        # Make the prediction
        prediction = model.predict(features)

        # Return the result in a new HTML page (optional)
        return render_template('result.html', prediction=prediction[0])

    except Exception as e:
        return jsonify({'error': str(e)}), 400

# Run the app
if __name__ == '__main__':
    app.run(debug=True)